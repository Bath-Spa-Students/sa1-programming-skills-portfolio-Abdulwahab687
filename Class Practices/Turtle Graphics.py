import turtle

# Draw a red circle
turtle.pensize(4)  # Set pen size
turtle.pencolor('Blue')  # Set pen color
turtle.circle(120)  # Draw a circle with a radius of 120

# Draw a yellow circle
turtle.pensize(4)  # Set pen size
turtle.pencolor('Black')  # Change pen color to yellow
turtle.circle(160)  # Draw a circle with a radius of 90